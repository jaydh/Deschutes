									<h3>Buying a car</h3>
									<p>When buying a car you have multiple options, for starters you can purchase a new or used car.
										Used cars you can also purchase from a dealer or private party. <br/>
										When you buy a new car from a dealership they will take care of all paperwork for you,
										you will still be responsible for the title fees though. This is of course the most expensive option,
										all cars will be new models, a new 2015 Toyota Prius Two will cost you at least $23,000 from a Toyota dealer.<br/>
										For used cars you have multiple options. Dealers will also sell used and older model cars.
										A used 2006 Toyota Matrix XRS from a Toyota dealership will cost around $9,000.
										The dealership is more reliable than a private party because they are under federal regulations to provide
										maintenance checks and provide 100% accurate information about the vehicle.
										They will also take care of most paperwork for you as well.
										You will still be responsible for paperwork and title fees though. <br/>
										Buying a used car from a private party is the most difficult option but can also be the most affordable.
										A website many people use to find used cars from both dealers and private parties is Craigslist
										https://seattle.craigslist.org/search/cta. Now be careful, Craigslist is known for scams.
										Many people will buy a car thinking they got a good deal then find out the car is broken and the facts
										about the car were lies. To avoid maintenance scams have a car you are interested in get a maintenance
										check at a mechanic. This can cost roughly $100, so be sure you want to buy the car.
										Also whenever you buy something on Craigslist make sure you meet the seller in a public place at all times
										to avoid danger. <br/>
										After purchasing a car from a private party you will need to take care of all paper work.
										This can be done at a Department of Licensing office (DOL). Fill out all information needed about yourself
										and the seller. At a DOL office a Licensing Agent can help you with all legal work for your car.
										You will also need to take care of License plate information that the agent can help you with. <br/><br/>
										Recourses: <br/>
