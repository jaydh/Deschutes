									<p>To get a drivers license first decide if you want to get a permit.
										A permit allows you to practice driving with another individual with a valid
										Washington State drivers license and 5+ years of driving experience.
										You also will get a permit if you enroll in a drivers training course. <br/>
										To get a permit you need to 15 years old and enrolled in a drivers training course.
										The training school will provide a waiver to get your permit.
										If you do not enroll in a drivers training course you need to be 15 Â½ years old and you will need
										to take a written knowledge test at a training school or testing facility.
										Once you have gotten your waiver or passed your written knowledge test you will need to go
										to a Department of Licensing office (DOL) to fill out forms. Documents you will need at the DOL office are <br/>
										-Passport <br/>
										-I-20 visa (or other visa)<br/>
										You will also need to prove you are a Washington State resident if you do not have a Social Security number.
										To do this you can bring in either/both<br/>
										- A letter addressed to you at your Washington residence<br/>
										- College transcripts from your Washington school<br/>
										Your residence will need to be verified, this process can take up to 2 weeks after filling out forms
										at the DOL. Also if you are under 18 years old you will also need to fill out
										the Parental Authorization Affidavit form. <br/>
										To get a Washington State drivers license you will need to first apply for a license at a DOL office,
										if you have a permit you have already done this step. If you are 16 years old you will need to have
										completed a drivers training course and passed their drive test to get your license.<br/>
										If you did not enroll in a drivers training course you will need to be at least 18 years old.
										You need to schedule and pass a drive test at a training and testing location.
										After passing your drive test you will need to return to the DOL office to finish paper work and
										receive your temporary license, your permanent license will be mailed to you shortly after. <br/>
