						<h2>Homestay</h2>
						<p>Pro<br/>
							Cost: homestay is sometimes cheaper way to live than living by yourself especially around
							Seattle because it includes meals, all utilities and rent. <br/>
							Cultural experiences: living with local provides you what it is like to live in the U.S. Some of
							the hosts are immigrants, and they will understand your feelings well.<br/>
							Home: you can construct a second home in the U.S. by homestay. They will welcome you
							as one of a family member, and you can feel family even your real family is far away.<br/>
							Studying: It is a good environment to concentrate on studying because you don’t have to do
							house works such as going to buy food or daily necessities, cleaning all the house, and
							making meals every day.<br/><br/>
							Con<br/>
							Rules: In many cases, the hosts make rules to live together and to make your and their life
							comfortable. You might feel too much restriction and miss free life from those rules.<br/>
							Difference of hosts: the hosts accepts foreign students for variable reasons. So it is
							unavoidable that there are difference between hosts. Some will treat you as real family and
							take care of you well, on the other hand, some accept the students as a business, so they
							don’t take care of you well or just do minimum necessary things. Your host can’t be exact
							the same as you expected.</p>

