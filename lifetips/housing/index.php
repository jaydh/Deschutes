						<p>There are many dorms in Seattle Washington, but most of them are limited to Universities such as:</p>
						<h3>University of Washington (UW)</h3>
						<p>The Residence Hall community is a great option for undergraduate students who will be studying
							at the UW for one academic year or less. In a typical year we expect to have autumn
							quarter housing for new undergraduate students who apply for residence hall housing by May 15.
							Residence hall assignments for the upcoming quarter are made during the previous quarter.
							Autumn quarter assignment notifications are sent beginning in early August, and for other
							quarters, about a month before the quarter begins.
							Most of the residence halls are closed during winter break (from 12 noon on December 19 to 8
							am on January 3) however, you have the option to request when you apply to be assigned to an
							area that is open for winter break (Lander Hall, Mercer Court, Stevens Court). We do our very
							best to assign international students who request winter break housing to one of the winter break
							halls.<br/>
							https://www.hfs.washington.edu/housing/rh/welcome/#gsc.tab=0
						</p>
